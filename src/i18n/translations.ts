// Get translation
export const t = (key: TranslationKey, lang: Language = 'ja'): string => {
    return translations[lang][key] || translations.ja[key] || key;
};

// Detect browser language
export const detectLanguage = (): Language => {
    const browserLang = navigator.language.split('-')[0];
    const supportedLangs: Language[] = ['ja', 'en', 'zh', 'ko', 'es', 'fr'];

    if (supportedLangs.includes(browserLang as Language)) {
        return browserLang as Language;
    }
    return 'ja'; // Default to Japanese
};
